func TestStoreProvider_Update_URL(t *testing.T) {
	p := webhooks.NewStoreProvider(openTestStore(t))
	sub := newTestSubscription("sub-upd", "svc", "https://old.example.com/hook", []string{"audit.write"})
	if err := p.Create(&sub); err != nil {
		t.Fatalf("Create: %v", err)
	}

	newURL := "https://new.example.com/hook"
	updated, err := p.Update(sub.ID, webhooks.SubscriptionPatch{URL: &newURL})
	if err != nil {
		t.Fatalf("Update: %v", err)
	}
	if updated.URL != newURL {
		t.Errorf("Update URL = %q, want %q", updated.URL, newURL)
	}
	if updated.ID != sub.ID {
		t.Errorf("Update changed ID from %q to %q", sub.ID, updated.ID)
	}
	if updated.CreatedAt != sub.CreatedAt {
		t.Errorf("Update changed CreatedAt")
	}
	// Verify persisted.
	got, _ := p.Get(sub.ID)
	if got.URL != newURL {
		t.Errorf("persisted URL = %q, want %q", got.URL, newURL)
	}
}

