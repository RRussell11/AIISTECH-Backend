func TestStoreProvider_ListPage(t *testing.T) {
	p := webhooks.NewStoreProvider(openTestStore(t))

	for i := range 5 {
		sub := newTestSubscription(fmt.Sprintf("sub-page-%02d", i), "svc", "https://example.com/hook", nil)
		if err := p.Create(&sub); err != nil {
			t.Fatalf("Create: %v", err)
		}
	}

	page1, cursor, err := p.ListPage("", 3)
	if err != nil {
		t.Fatalf("ListPage(page1): %v", err)
	}
	if len(page1) != 3 {
		t.Errorf("page1 = %d records, want 3", len(page1))
	}
	if cursor == "" {
		t.Error("expected non-empty cursor after first page")
	}

	page2, cursor2, err := p.ListPage(cursor, 3)
	if err != nil {
		t.Fatalf("ListPage(page2): %v", err)
	}
	if len(page2) != 2 {
		t.Errorf("page2 = %d records, want 2", len(page2))
	}
	if cursor2 != "" {
		t.Errorf("expected empty cursor after last page, got %q", cursor2)
	}
}

