func TestStoreProvider_Update_UpdatedAt_Changes(t *testing.T) {
	p := webhooks.NewStoreProvider(openTestStore(t))
	sub := newTestSubscription("sub-ts", "svc", "https://example.com/hook", nil)
	sub.UpdatedAt = time.Now().UTC().Add(-1 * time.Hour)
	_ = p.Create(&sub)

	before := sub.UpdatedAt
	newURL := "https://new.example.com"
	updated, _ := p.Update(sub.ID, webhooks.SubscriptionPatch{URL: &newURL})
	if !updated.UpdatedAt.After(before) {
		t.Errorf("UpdatedAt was not bumped: before=%v, after=%v", before, updated.UpdatedAt)
	}
}

